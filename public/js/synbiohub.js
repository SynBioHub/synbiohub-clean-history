$('.sbh-download-picture').click(function() {

    open("data:image/svg+xml," + encodeURIComponent(createSVGDocument()));

})

function createSVGDocument() {

    var element = document.getElementById('design')

    var svgElementHtml = element.innerHTML;

    if(svgElementHtml.indexOf('<svg') !== -1) {

        var tagClose = svgElementHtml.indexOf('>');

        svgElementHtml = svgElementHtml.slice(tagClose + 1).split('</svg>').join('');
    }

    var w = element.clientWidth
    var h = element.clientHeight

    return [
        '<?xml version="1.0" encoding="iso-8859-1"?>',
        '<!DOCTYPE svg PUBLIC "-//W3C//DTD SVG 1.1//EN" "http://www.w3.org/Graphics/SVG/1.1/DTD/svg11.dtd">',
        '<svg version="1.1" xmlns="http://www.w3.org/2000/svg" xmlns:xlink="http://www.w3.org/1999/xlink" x="0px" y="0px"',
             'width="' + w + 'px" height="' + h + 'px" ' +
                'viewBox="0 0 ' + w + ' ' + h + '" xml:space="preserve">',

        '<title>' + window.title + '</title>',
        '<desc>Generated by SynBioHub http://synbiohub.org</desc>',

         svgElementHtml,

        '</svg>'

    ].join('\n');
}

